                            <h2>第2章 安全管理措置</h2>

                            <h3>
                                第1節<br>
                                組織的安全管理措置
                            </h3>

                            <p>
                                <span class="fw-b">【事務取扱責任者等】<br>第3条</span>
                            </p>
                            <ol>
                                <li>人事部を当社における個人データの取扱いに関する責任部署とする。</li>
                                <li>当社に、事務取扱責任者1人を置く。</li>
                                <li>事務取扱責任者には、人事部長をもってこれに充てるものとする。</li>
                                <li>部門責任者には、個人データを取り扱う各部・各施設における部長・支配人がその任にあたる。</li>
                            </ol>

                            <p>
                                <span class="fw-b">【事務取扱責任者等の任務】<br>第4条</span>
                            </p>
                            <ol>
                                <li>
                                事務取扱責任者は、当社における個人情報の取得及び個人データの保護管理に関する業務を統括するとともに、本規程に定められた事項を理解し、遵守し、また、事務取扱担当者に本規程を理解させ、遵守させるための教育訓練、安全対策の実施並びに周知徹底等の措置を実施する責任を負う。
                                </li>
                                <li>
                                事務取扱責任者は、次の業務を所掌する。<br>
                                <p class="ti_15" style="margin:0;">&#9312; 本規程及び委託先（個人データの取扱いの全部又は一部を委託する場合の委託を受けた者をいう。以下同じ。）の選定基準の承認及び周知</p>
                                <p class="ti_15" style="margin:0;">&#9313; 個人データの安全管理に関する教育・研修の企画・実施</p>
                                <p class="ti_15" style="margin:0;">&#9314; 個人データの利用申請の承認及び記録等の管理</p>
                                <p class="ti_15" style="margin:0;">&#9315; 管理区域及び取扱区域の設定</p>
                                <p class="ti_15" style="margin:0;">&#9316; 個人データの取扱区分及び権限についての設定及び変更の管理</p>
                                <p class="ti_15" style="margin:0;">&#9317; 個人データの取扱状況の把握</p>
                                <p class="ti_15" style="margin:0;">&#9318; 委託先における個人データの取扱状況等の監督</p>
                                <p class="ti_15" style="margin:0;">&#9319; その他当社における個人データの安全管理に関すること</p>
                                </li>
                                <li>
                                部門責任者である各部門における部長・支配人は、当該門における個人情報の取得及び個人データを適切に管理する任に当たり、個人データの適切な管理のために必要な措置を講じ、個人データの安全確保に努める責任を負う。
                                </li>
                                <li>
                                事務取扱責任者は、法令遵守の観点から、各部門の部門責任者に対して指導、助言する。
                                </li>
                            </ol>

                            <p>
                                <span class="fw-b">【事務取扱担当者等の監督】<br>第5条</span>
                            </p>
                            <ol>
                                <li>
                                事務取扱責任者は、個人データが本規程に基づき適正に取り扱われるよう、事務取扱担当者に対して必要かつ適切な監督を行うものとする。
                                </li>
                                <li>
                                各部門の部門責任者は、当該各部門の事務取扱担当者に対して必要かつ適切な監督を行い、事務取扱責任者に対して必要な報告を行う。
                                </li>
                            </ol>

                            <p>
                                <span class="fw-b">【事務取扱担当者の責務】<br>第6条</span>
                            </p>
                            <ol>
                                <li>
                                事務取扱担当者は、当社の個人データの取扱い又は委託処理等、個人データを取扱う業務に従事する際、法令等、本規程及びその他の社内規程並びに事務取扱責任者の指示した事項に従い、個人データの保護に十分な注意を払ってその業務を行うものとする。
                                </li>
                                <li>
                                事務取扱担当者は、個人情報の漏えい等、法令等、本規程並びにその他の社内規程に違反している事実又は兆候を把握した場合、速やかに所属部門の部門責任者又は事務取扱責任者に報告するものとする。所属部門の事務取扱担当者から、当該報告を受けた部門責任者は速やかに事務取扱責任者に報告するものとする。
                                </li>
                            </ol>

                            <p>
                                <span class="fw-b">【本規程に基づく運用状況の記録】<br>第7条</span><br>
                            事務取扱担当者は、本規程に基づく運用状況を確認するため、以下の項目につき、当社所定のチェックリストに基づき確認し、記入済みのチェックリストを保存するものとする。

                            <p class="ti_15" style="margin:0;">&#9312; 個人情報の取得及び個人情報データベース等ファイルへの入力状況</p>
                            <p class="ti_15" style="margin:0;">&#9313; 個人情報データベース等の利用・出力状況の記録</p>
                            <p class="ti_15" style="margin:0;">&#9314; 個人データが記載又は記録された書類・媒体等の持ち運び（個人データを、管理区域又は取扱区域の外へ移動させることをいい、事業所内での移動等も含まれる。以下同じ。）等の状況</p>
                            <p class="ti_15" style="margin:0;">&#9315; 個人情報データベース等の削除・廃棄記録</p>
                            <p class="ti_15" style="margin:0;">&#9316; 削除・廃棄を委託した場合、これを証明する記録等</p>
                            <p class="ti_15" style="margin:0;">&#9317; 個人情報データベース等を情報システムで取り扱う場合、事務取扱担当者の情報システムの利用状況（ログイン実績、アクセスログ等）</p>
                            </p>

                            <p>
                                <span class="fw-b">【取扱状況の確認手段】<br>第8条</span><br>
                            事務取扱担当者は、個人情報データベース等の取扱状況を確認するための手段として、当社所定の個人情報管理台帳に以下の事項を記録するものとする。なお、個人情報管理台帳には、個人データ自体は記載しないものとする。

                            <p class="ti_15" style="margin:0;">&#9312; 個人情報データベース等の種類、名称</p>
                            <p class="ti_15" style="margin:0;">&#9313; 個人データの範囲</p>
                            <p class="ti_15" style="margin:0;">&#9314; 利用目的</p>
                            <p class="ti_15" style="margin:0;">&#9315; 記録媒体</p>
                            <p class="ti_15" style="margin:0;">&#9316; 保管場所（管理区域）</p>
                            <p class="ti_15" style="margin:0;">&#9317; 責任者</p>
                            <p class="ti_15" style="margin:0;">&#9318; 取扱部署</p>
                            <p class="ti_15" style="margin:0;">&#9319; 事務取扱担当者（アクセス権者）</p>
                            <p class="ti_15" style="margin:0;">&#9320; 保存期間</p>
                            <p class="ti_15" style="margin:0;">&#9321; 削除・廃棄方法</p>
                            </p>

                            <p>
                                <span class="fw-b">【情報漏えい事態への対応】<br>第9条</span><br>
                                事務取扱責任者は、規則第6条の2に定める個人データの漏えい、滅失又は毀損（以下「漏えい等」という。）の事態が発生した場合、事実関係の調査及び原因の究明に必要な措置を講じるとともに、規則第6条の3の定めに従い個人情報保護委員会に対して報告し、規則第6条の5の定めに従い本人に対して通知するものとする。また、調査結果に基づいて再発防止策の検討を行い、再発防止策の決定後速やかに事実関係及び再発防止策を公表するものとする。
                            </p>

                            <p>
                                <span class="fw-b">【苦情への対応】<br>第10条</span><br>
                                事務取扱担当者は、法、ガイドライン又は本規程に関し、本人から苦情の申出を受けた場合には、その旨を部門責任者に報告する。報告を受けた部門責任者は、適切に対応するものとする。
                            </p>

                            <p>
                                <span class="fw-b">【取扱状況の確認並びに安全管理措置の見直し】<br>第11条</span><br>
                            </p>
                            <ol>
                                <li>
                                事務取扱責任者は、1年に1回以上の頻度で又は臨時に第7条に規定する個人データの運用状況の記録及び第8条に規定する個人情報データベース等の取扱状況の確認を実施しなければならない。
                                </li>
                                <li>
                                事務取扱責任者は、前項の確認の結果及び次条の監査の結果に基づき、安全管理措置の評価、見直し及び改善に取り組むものとする。
                                </li>
                            </ol>

                            <p>
                                <span class="fw-b">【監査】<br>第12条</span><br>
                                代表取締役が指名した者は、当社の個人データの適正な取扱いその他法令及び本規則の遵守状況について検証し、その改善を事務取扱責任者及び各部の部門責任者に促す。
                            </p>

                            <h3>
                                第2節<br>
                                人的安全管理措置
                            </h3>

                            <p>
                                <span class="fw-b">【教育・研修】<br>第13条</span><br>
                            </p>
                            <ol>
                                <li>
                                事務取扱責任者は、本規程に定められた事項を理解し、遵守するとともに、従業者に本規程を遵守させるための教育訓練を企画・運営する責任を負う。
                                </li>
                                <li>
                                従業者は、事務取扱責任者が主催する本規程を遵守させるための教育を受けなければならない。研修の内容及びスケジュールは、事業年度毎に事務取扱責任者が定める。
                                </li>
                                <li>
                                    当社は、個人データについての秘密保持に関する事項を就業規則に盛り込むものとする。
                                </li>
                            </ol>

                            <h3>
                                第3節<br>
                                物理的安全管理措置
                            </h3>

                            <p>
                            <span class="fw-b">【個人データを取り扱う区域の管理】<br>第14条</span><br>
