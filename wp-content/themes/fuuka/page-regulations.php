                                    当社は、前項の規定に基づき求められた保有個人データの利用目的を通知しない旨の決定をしたときは、本人に対し、遅滞なく、その旨を通知するものとする。
                                </li>
                            </ol>

                            <p>
                                <span class="fw-b">【保有個人データの開示】<br>第34条</span>
                            </p>

                            <ol>
                                <li>
                                    本人からの当該本人が識別される保有個人データ開示の請求の方法は、&#9312;電磁的記録の提供による方法、&#9313;書面の交付による方法、③その他当社が定める方法とする。
                                </li>
                                <li>
                                    当社は、本人から、当該本人が識別される保有個人データの開示に係る請求を受けたときは、当該本人が請求した方法（当該方法による開示に多額の費用を要する場合その他 の当該方法による開示が困難である場合にあっては、書面の交付による方法）により、 遅滞なく、当該保有個人データを開示するものとする。ただし、開示することにより次の各号のいずれかに該当する場合は、その全部又は一部を開示しないことができる。
                                    <p class="ti_2" style="margin:0;">一. 本人又は第三者の生命、身体、財産その他の権利利益を害するおそれがある場合</p>
                                    <p class="ti_2" style="margin:0;">二. 当社の業務の適正な実施に著しい支障を及ぼすおそれがある場合</p>
                                    <p class="ti_2" style="margin:0;">三. 他の法令に違反することとなる場合</p>
                                </li>
                                <li>
                                    当社は、前項の規定に基づき求められた保有個人データの全部若しくは一部について開示しない旨の決定をしたとき、又は第1項の規定により本人が請求した方法による開示が困難であるときは、本人に対し、遅滞なく、その旨を通知するものとする。この場合、当社は本人に対して、当該通知においてその理由を説明するものとする。
                                </li>
                                <li>
                                    他の法令の規定により、本人に対し第2項本文に規定する方法に相当する方法により当該本人が識別される保有個人データの全部又は一部を開示することとされている場合には、当該全部又は一部の保有個人データについては、同項の規定は、適用しない。
                                </li>
                                <li>
                                    第1項から第3項までの規定は、当該本人が識別される個人データに係る第 30 条第1項及び第 31 条第4項の記録（次の各号に掲げるものを除く。以下「第三者提供記録」という。）について準用する。
                                    <p class="ti_2" style="margin:0;">一. 当該記録の存否が明らかになることにより、本人又は第三者の生命、身体又は財産に危害が及ぶおそれがあるもの</p>
                                    <p class="ti_2" style="margin:0;">二. 当該記録の存否が明らかになることにより、違法又は不当な行為を助長し、又は誘発するおそれがあるもの</p>
                                    <p class="ti_2" style="margin:0;">三. 当該記録の存否が明らかになることにより、国の安全が害されるおそれ、他国若しくは国際機関との信頼関係が損なわれるおそれ又は他国若しくは国際機関との交渉上不利益を被るおそれがあるもの</p>
                                    <p class="ti_2" style="margin:0;">四. 当該記録の存否が明らかになることにより、犯罪の予防、鎮圧又は捜査その他の公共の安全と秩序の維持に支障が及ぶおそれがあるもの</p>
                                </li>
                            </ol>

                            <p>
                                <span class="fw-b">【保有個人データの訂正等】<br>第35条</span>
                            </p>

                            <ol>
                                <li>
                                    当社は、当該本人が識別される保有個人データの内容が事実でないことを理由に当該本人から訂正、追加又は削除（以下「訂正等」という。）に係る請求を受けた場合には、その内容の訂正等に関して他の法令の規定により特別の手続が定められている場合を除き、利用目的の達成に必要な範囲において、遅滞なく必要な調査を行い、その結果に基づき、当該保有個人データの内容の訂正等を行うものとする。
                                </li>
                                <li>
                                    当社は、前項の請求に係る保有個人データの内容の全部又は一部について訂正等を行ったとき、又は訂正等を行わない旨を決定したときは、本人に対し、遅滞なく、その旨（訂正等を行ったときは、その内容を含む。）を通知するものとする。この場合、当社は本人に対して、当該通知においてその理由を説明するものとする。
                                </li>
                            </ol>

                            <p>
                                <span class="fw-b">【保有個人データの利用停止等】<br>第36条</span>
                            </p>

                            <ol>
                                <li>
                                    当社は、本人から、当該本人が識別される保有個人データが、第 23 条の規定（利用目的による制限）に違反して取得されているという理由、第 25 条の規定（不適正な利用の禁止）、第 26 条（適正な取得）の規定に違反して取り扱われたものであるという理由によって、当該保有個人データの利用の停止、消去（以下、本条において「利用停止等」という。）に係る請求を受けた場合であって、利用停止等に理由があることが判明したときは、違反を是正するために必要な限度で、遅滞なく、保有個人データの利用停止等を行うものとする。ただし、利用停止等を行うことに多額の費用を要する場合その他の利用停止等を行うことが困難な場合であって、当該本人の権利利益を保護するため必要なこれに代わるべき措置をとるときは、この限りではない。
                                </li>
                                <li>
                                    当社は、本人から、当該本人が識別される保有個人データが第 28 条第1項又は第 29条の規定に違反して第三者に提供されているという理由によって、当該保有個人データの第三者への提供の停止に係る請求を受けた場合であって、その請求に理由があることが判明したときは、遅滞なく、当該保有個人データの提供を停止するものとする。ただし、当該保有個人データの第三者への提供の停止に多額の費用を要する場合その他の第三者への提供を停止することが困難な場合であって、本人の権利利益を保護するため必要なこれに代わるべき措置をとるときは、この限りでない。
                                </li>
                                <li>
                                    当社は、第一項の規定に基づき求められた保有個人データの全部若しくは一部について利用停止等を行ったとき若しくは利用停止等を行わない旨の決定をしたとき、又は前項の規定に基づき求められた保有個人データの全部若しくは一部について第三者への提供を停止したとき若しくは第三者への提供を停止しない旨の決定をしたときは、本人に対し、遅滞なく、その旨を通知するものとする。この場合、当社は本人に対して、当該通知においてその理由を説明するものとする。
                                </li>
                                <li>
                                    &#9312;当該本人が識別される保有個人データを当該個人情報取扱事業者が利用する必要がなくなった場合、&#9313;当該本人が識別される保有個人データについて、第9条（情報漏えい事態への対応）に規定する事態が生じた場合③その他当該本人が識別される保有個人データの取扱いにより当該本人の権利又は正当な利益が害されるおそれがある場合であって、かつ、本人から当該保有個人データの利用停止等又は第三者への提供の停止に係る請求があった場合には、当社は、本人の権利利益の侵害を防止するために必要な限度で、遅滞なく、当該保有個人データの利用停止等又は第三者への提供の停止を行うものとする。ただし、当該保有個人データの利用停止等又は第三者への提供の停止に多額の費用を要する場合その他の利用停止等又は第三者への提供の停止を行うことが困難な場合であって、本人の権利利益を保護するため必要なこれに代わるべき措置をとるときは、この限りでない。
                                </li>
                            </ol>

                            <p>
                                <span class="fw-b">【苦情処理】<br>第37条</span>
                            </p>

                            <ol>
                                <li>
                                    当社は、当社における保有個人データの取扱いに関する苦情の適切かつ迅速な処理に努めるものとする。
                                </li>
                                <li>
                                    苦情処理に関する当社の体制整備は、第 10 条に定めるところに従う。
                                </li>
                            </ol>

                            <h2>第5章 個人データの委託の取扱い</h2>

                            <p>
                                <span class="fw-b">【委託先における安全管理措置】<br>第38条</span>
                            </p>

                            <ol>
                                <li>
                                    当社は、個人データの全部又は一部の委託する場合には、当社自らが果たすべき安全管理措置と同等の措置が委託先において適切に講じられるよう、必要かつ適切な監督を行うものとする。
                                </li>
                                <li>
                                    前項の「必要かつ適切な監督」には次に掲げる事項が含まれる。
                                    <p class="ti_3" style="margin:0;">（1）委託先の適切な選定</p>
                                    <p class="ti_3" style="margin:0;">（2）委託先に安全管理措置を遵守させるために必要な契約の締結</p>
                                    <p class="ti_3" style="margin:0;">（3）委託先における個人データの取扱状況の把握</p>
                                </li>
                                <li>
                                    前項（1）の「委託先の適切な選定」に当たっては、委託先の安全管理措置が、少なくとも法第 20 条及び「個人情報の保護に関する法律についてのガイドライン（通則編）」で委託元に求められるものと同等であることを確認するため、同ガイドライン「8（（別添）講ずべき安全管理措置の内容）」に定める各項目が、委託する業務内容に沿って、確実に実施されることについて、あらかじめ確認しなければならない。
                                </li>
                                <li>
                                    第2項（3）の「委託先における個人データの取扱状況の把握」については、委託契約の内容として、以下の規定等を盛り込むものとする。
                                    <p class="ti_15" style="margin:0;">&#9312; 秘密保持義務に関する規定</p>
                                    <p class="ti_15" style="margin:0;">&#9313; 事業所内からの個人データの持出しの禁止</p>
                                    <p class="ti_15" style="margin:0;">&#9314; 個人データの目的外利用の禁止</p>
                                    <p class="ti_15" style="margin:0;">&#9315; 再委託における条件</p>
                                    <p class="ti_15" style="margin:0;">&#9316; 漏えい事案等が発生した場合の委託先の責任に関する規定</p>
                                    <p class="ti_15" style="margin:0;">&#9317; 委託契約終了後の個人データの返却又は廃棄に関する規定</p>
                                    <p class="ti_15" style="margin:0;">&#9318; 従業者に対する監督・教育に関する規定</p>
                                    <p class="ti_15" style="margin:0;">&#9319; 契約内容の遵守状況について報告を求める規定</p>
                                    <p class="ti_15" style="margin:0;">&#9320; 個人データを取り扱う従業者の明確化に関する規定</p>
                                    <p class="ti_15" style="margin:0;">&#9321; </p>
                                </li>
                                <li>
                                    当社は、委託先の管理については、人事部を責任部署とする。
                                </li>
                                <li>
                                    当社は、委託先において個人データの安全管理が適切に行われていることについて、必要に応じてモニタリングをするものとする。
                                </li>
                                <li>
                                    当社は、委託先において情報漏えい事故等が発生した場合に、適切な対応がなされ、速やかに当社に報告される体制になっていることを確認するものとする。
                                </li>
                                <li>
                                    委託先は、当社の許諾を得た場合に限り、委託を受けた個人データの全部又は一部を再委託することができるものとする。再委託先が更に再委託する場合も同様とする。
                                </li>
                                <li>
                                    当社は、再委託先の適否の判断のみならず、委託先が再委託先に対しても必要かつ適切な監督を行っているかどうかについても監督する。
                                </li>
                                <li>
                                    当社は、委託先が再委託をする場合、当該再委託契約の内容として、第4項と同等の規定等を盛り込ませるものとする。
                                </li>
                            </ol>

